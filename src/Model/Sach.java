package Model;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class Sach implements Serializable{

    public Sach(int maSach, String tenSach, String tacGia, String chuyenNganh, int namSanXuat, int soLuong) {
        this.maSach = maSach;
        this.tenSach = tenSach;
        this.tacGia = tacGia;
        this.chuyenNganh = chuyenNganh;
        this.namSanXuat = namSanXuat;
        this.soLuong = soLuong;
    }
    int maSach;
    String tenSach, tacGia, chuyenNganh;
    int namSanXuat, soLuong;

    public int getMaSach() {
        return maSach;
    }

    public void setMaSach(int maSach) {
        this.maSach = maSach;
    }

    public String getTenSach() {
        return tenSach;
    }

    public void setTenSach(String tenSach) {
        this.tenSach = tenSach;
    }

    public String getTacGia() {
        return tacGia;
    }

    public void setTacGia(String tacGia) {
        this.tacGia = tacGia;
    }

    public String getChuyenNganh() {
        return chuyenNganh;
    }

    public void setChuyenNganh(String chuyenNganh) {
        this.chuyenNganh = chuyenNganh;
    }

    public int getNamSanXuat() {
        return namSanXuat;
    }

    public void setNamSanXuat(int namSanXuat) {
        this.namSanXuat = namSanXuat;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }
    
    public Object[] toObjects(){
        return new Object[]{maSach, tenSach, tacGia, chuyenNganh, namSanXuat, soLuong};
    }
    
    
}
